     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //dd($request->all());die();

        $fileName = '';
        if($request->document->getClientOriginalName()){
            $file = str_replace(' ','',$request->document->getClientOriginalName());
            $fileName = date('mYdHs').rand(1,999).'_'.$file;
            $request->document->storeAs('public/jadwal', $fileName);
        }

        $user = Jadwal::create(array_merge($request->all(),[
           'document' => $fileName
        ]));
        return redirect('jadwal');
        
    }

